package com.tle.web.selection.contribute;

public class QuickUploadContributeModel
{
	private boolean quickUpload;
	private boolean contribute;

	public void setQuickUpload(boolean quickUpload)
	{
		this.quickUpload = quickUpload;
	}

	public boolean isQuickUpload()
	{
		return quickUpload;
	}

	public void setContribute(boolean contribute)
	{
		this.contribute = contribute;
	}

	public boolean isContribute()
	{
		return contribute;
	}
}
